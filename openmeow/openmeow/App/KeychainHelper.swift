import Foundation
import Security

/// Provides secure Keychain-based storage for sensitive values like auth tokens.
nonisolated enum KeychainHelper {
    private static let service = AppConstants.bundleID

    static func save(key: String, value: String) {
        let data = Data(value.utf8)
        let query: [String: Any] = [
            kSecClass as String: kSecClassGenericPassword,
            kSecAttrService as String: service,
            kSecAttrAccount as String: key,
        ]
        SecItemDelete(query as CFDictionary)
        var addQuery = query
        addQuery[kSecValueData as String] = data
        SecItemAdd(addQuery as CFDictionary, nil)
    }

    static func load(key: String) -> String? {
        let query: [String: Any] = [
            kSecClass as String: kSecClassGenericPassword,
            kSecAttrService as String: service,
            kSecAttrAccount as String: key,
            kSecReturnData as String: true,
            kSecMatchLimit as String: kSecMatchLimitOne,
        ]
        var result: AnyObject?
        guard SecItemCopyMatching(query as CFDictionary, &result) == errSecSuccess,
              let data = result as? Data else { return nil }
        return String(data: data, encoding: .utf8)
    }

    static func delete(key: String) {
        let query: [String: Any] = [
            kSecClass as String: kSecClassGenericPassword,
            kSecAttrService as String: service,
            kSecAttrAccount as String: key,
        ]
        SecItemDelete(query as CFDictionary)
    }

    /// Migrate a value from UserDefaults to Keychain (one-time migration).
    static func migrateFromUserDefaults(key: String) {
        if let existing = UserDefaults.standard.string(forKey: key), !existing.isEmpty {
            if load(key: key) == nil {
                save(key: key, value: existing)
            }
            UserDefaults.standard.removeObject(forKey: key)
        }
    }
}
